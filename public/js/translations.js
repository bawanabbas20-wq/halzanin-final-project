function normalizeTranslationKey(key) {
  return legacyTranslationKeys[key] || key;
}

function interpolate(value, replacements) {
  return String(value || '').replace(/\{(\w+)\}/g, (match, name) => {
    return replacements && Object.prototype.hasOwnProperty.call(replacements, name) ? replacements[name] : match;
  });
}

function i18n(key, replacements, lang) {
  lang = lang || document.documentElement.lang || localStorage.getItem('lang') || 'en';
  lang = lang === 'ku' ? 'ku' : 'en';
  const normalized = normalizeTranslationKey(key);
  const value = window.translations[lang] && window.translations[lang][normalized]
    ? window.translations[lang][normalized]
    : (window.translations.en && window.translations.en[normalized]) || key;
  return interpolate(value, replacements);
}

function i18nDocument(label, replacements, lang) {
  return i18n(documentTranslationKeys[label] || label, replacements, lang);
}

function setTranslatedText(el, value) {
  if (!el.children.length) {
    el.textContent = value;
    return;
  }

  const label = el.querySelector('[data-i18n-label]');
  if (label) {
    label.textContent = value;
    return;
  }

  const textNodes = Array.from(el.childNodes).filter((node) => (
    node.nodeType === Node.TEXT_NODE && node.textContent.trim()
  ));

  if (textNodes.length) {
    textNodes[0].textContent = value;
    textNodes.slice(1).forEach((node) => { node.textContent = ''; });
    return;
  }

  el.textContent = value;
}

