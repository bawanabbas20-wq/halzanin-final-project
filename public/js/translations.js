function normalizeTranslationKey(key) {
  return legacyTranslationKeys[key] || key;
}

function interpolate(value, replacements) {
  return String(value || '').replace(/\{(\w+)\}/g, (match, name) => {
    return replacements && Object.prototype.hasOwnProperty.call(replacements, name) ? replacements[name] : match;
  });
}

function i18n(key, replacements, lang) {
  lang = lang || document.documentElement.lang || localStorage.getItem('lang') || 'en';
  lang = lang === 'ku' ? 'ku' : 'en';
  const normalized = normalizeTranslationKey(key);
  const value = window.translations[lang] && window.translations[lang][normalized]
    ? window.translations[lang][normalized]
    : (window.translations.en && window.translations.en[normalized]) || key;
  return interpolate(value, replacements);
}

function i18nDocument(label, replacements, lang) {
  return i18n(documentTranslationKeys[label] || label, replacements, lang);
}

function i18nMessage(message, lang) {
  const offDaysMatch = String(message || '').match(/^(\d+) off day\(s\) added successfully\.$/);
  if (offDaysMatch) {
    return i18n('message.off_days_added', { count: offDaysMatch[1] }, lang);
  }
  const appointmentMatch = String(message || '').match(/^Appointment booked\. Your tracking code is (.+)\.$/);
  if (appointmentMatch) {
    return i18n('message.appointment_booked', { code: appointmentMatch[1] }, lang);
  }
  return i18n(messageTranslationKeys[message] || message, {}, lang);
}

function setTranslatedText(el, value) {
  if (!el.children.length) {
    el.textContent = value;
    return;
  }

  const label = el.querySelector('[data-i18n-label]');
  if (label) {
    label.textContent = value;
    return;
  }

  const textNodes = Array.from(el.childNodes).filter((node) => (
    node.nodeType === Node.TEXT_NODE && node.textContent.trim()
  ));

  if (textNodes.length) {
    textNodes[0].textContent = value;
    textNodes.slice(1).forEach((node) => { node.textContent = ''; });
    return;
  }

  el.textContent = value;
}

const autoTranslatedTextNodes = new WeakMap();
const autoTranslatedAttributes = new WeakMap();

function getAutoTranslationKey(text) {
  const normalized = normalizeTranslationKey(text);
  if (window.translations.en && Object.prototype.hasOwnProperty.call(window.translations.en, normalized)) {
    return normalized;
  }
  if (window.translations.ku && Object.prototype.hasOwnProperty.call(window.translations.ku, normalized)) {
    return normalized;
  }
  return null;
}

function translatePlainTextNode(node, lang) {
  const original = autoTranslatedTextNodes.get(node) || node.textContent.trim();
  const key = getAutoTranslationKey(original);
  if (!key) return;

  autoTranslatedTextNodes.set(node, original);
  const value = window.translations[lang] && window.translations[lang][key];
  if (!value) return;

  const leading = (node.textContent.match(/^\s*/) || [''])[0];
  const trailing = (node.textContent.match(/\s*$/) || [''])[0];
  node.textContent = `${leading}${value}${trailing}`;
}

function translatePlainAttribute(el, attr, lang) {
  if (!el.hasAttribute(attr)) return;
  let originals = autoTranslatedAttributes.get(el);
  if (!originals) {
    originals = {};
    autoTranslatedAttributes.set(el, originals);
  }
  const original = originals[attr] || el.getAttribute(attr).trim();
  const key = getAutoTranslationKey(original);
  if (!key) return;

  originals[attr] = original;
  const value = window.translations[lang] && window.translations[lang][key];
  if (value) el.setAttribute(attr, value);
}

function applyPlainTextTranslations(lang) {
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      if (!node.textContent.trim()) return NodeFilter.FILTER_REJECT;
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      if (parent.closest('script, style, noscript, [data-i18n], [data-i18n-placeholder], [data-i18n-title], [data-en], [data-ku]')) {
        return NodeFilter.FILTER_REJECT;
      }
      return getAutoTranslationKey(autoTranslatedTextNodes.get(node) || node.textContent.trim())
        ? NodeFilter.FILTER_ACCEPT
        : NodeFilter.FILTER_REJECT;
    },
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => translatePlainTextNode(node, lang));

  document.querySelectorAll('[aria-label], [title], [placeholder]').forEach((el) => {
    if (el.closest('[data-en], [data-ku]')) return;
    if (!el.hasAttribute('data-i18n-title')) translatePlainAttribute(el, 'title', lang);
    if (!el.hasAttribute('data-i18n-placeholder')) translatePlainAttribute(el, 'placeholder', lang);
    translatePlainAttribute(el, 'aria-label', lang);
  });
}

function applyLanguage(lang) {
  lang = lang === 'ku' ? 'ku' : 'en';
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ku' ? 'rtl' : 'ltr';
  document.documentElement.style.fontFamily = lang === 'ku' ? "'Noto Naskh Arabic', serif" : "'Outfit', sans-serif";
  document.documentElement.classList.toggle('font-arabic', lang === 'ku');
  document.documentElement.classList.toggle('font-outfit', lang !== 'ku');

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = normalizeTranslationKey(el.getAttribute('data-i18n'));
    const replacements = {};
    if (el.hasAttribute('data-i18n-count')) replacements.count = el.getAttribute('data-i18n-count');
    const value = window.translations[lang] && window.translations[lang][key]
      ? interpolate(window.translations[lang][key], replacements)
      : null;
    if (value) setTranslatedText(el, value);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = normalizeTranslationKey(el.getAttribute('data-i18n-placeholder'));
    const value = window.translations[lang] && window.translations[lang][key];
    if (value) el.placeholder = value;
  });

  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = normalizeTranslationKey(el.getAttribute('data-i18n-title'));
    const value = window.translations[lang] && window.translations[lang][key];
    if (value) el.title = value;
  });

  applyPlainTextTranslations(lang);

  localStorage.setItem('lang', lang);
  localStorage.setItem('halzanin-lang', lang);
  syncHalzaninLanguageToggle(lang);

  if (typeof window.updateChatQuickPrompts === 'function') {
    window.updateChatQuickPrompts(lang);
  }

  document.dispatchEvent(new CustomEvent('halzanin-language-changed', { detail: { lang } }));
}

